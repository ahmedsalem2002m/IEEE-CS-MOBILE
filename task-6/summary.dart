


// في البرمجة الكائنية التوجه OOP، فيه كذا مفهوم أساسي زي الوراثة Inheritance، التجريد Abstraction، وتعدد الأشكال Polymorphism.

// 1. **Abstraction**:
//    - هو إنك تخبي التفاصيل اللي ملهاش لازمة وتظهر الحاجات المهمة بس.
//    - بنعمل ده باستخدام **abstract class** اللي فيها دوال من غير تنفيذ، والكلاسات اللي بترث منها لازم تنفذ الدوال دي.

// 2. **Inheritance**:
//    - بتخلي كلاس معين ياخد خصائص ودوال من كلاس تاني، وده بيسهل إعادة استخدام الكود.
//    - بنستخدم الكلمة `extends` في لغات زي Dart عشان نعمل الوراثة.

// 3. **Polymorphism**:
//    - معناه إن نفس الدالة تشتغل بطرق مختلفة حسب الكائن اللي بيستخدمها.
//    - بنحقق ده باستخدام `override` في الكلاسات المشتقة، عشان نقدر نعدل تنفيذ الدوال اللي موجودة في الكلاس الأب.

// المفاهيم دي بتساعد في ترتيب الكود، وإعادة استخدامه بسهولة، وبتقلل التعقيد في المشاريع الكبيرة.